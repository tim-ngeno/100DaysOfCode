# LOGICAL OPERATORS

# AND  - ALL CONDITIONS HAVE TO BE TRUE FOR THE CONDITION TO BE TRUE
a = 12
print(a > 10 and a < 13)
print(a > 14 and a < 15)

# OR - AT LEAST ONE OF THE CONDITIONS HAS TO BE TRUE TO EVALUATE AS TRUE
print(a > 13 or a < 10)
print(a < 14 or a > 15)

# NOT - REVERSES THE CONDITION
print(not a > 12)
