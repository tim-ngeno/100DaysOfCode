# Data Types
# String - A string of characters e.g

"Hello"

# Pulling a character from a script is called subscripting
print("Hello"[3]) # should print the value at index 3, starting from 0

# Integer - Whole numbers (negative and positive)
print(123 + 456)

# Float - decimal numbers
3.14159

# Boolean
True
False


name = len(input("What is your name?\n"))
length = str(name) # changing type of len to string aka type casting

print("Your name has " + length + " characters")
